//Java program for addition of different data types.


class AdditionOfDataTypes
{
    
    public static void main(String[] aofdt)
    {
        // ASCII value of 'A' is 65
        char a = 'A'; 
	int b = 3; 


        // In Java, when char is involved in arithmetic operations, its ASCII value is used
        // ASCII value of 'A' (65) + 3 = 68

	System.out.println(a + b); 
    }
}



/* Output:
   Compile time: Success
   Run time: 68
*/
